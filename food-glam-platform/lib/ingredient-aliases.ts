/**
 * Expand a search query by resolving any known aliases to their English canonical.
 * Returns all unique terms (original + resolved canonicals) for use in search scoring.
 *
 * Examples:
 *   expandSearchTerms("ardei rosii")     → ["ardei","rosii","pepper","tomato"]
 *   expandSearchTerms("poivron rouge")   → ["poivron","rouge","red pepper","red","pepper","bell pepper"]
 *   expandSearchTerms("hühnchen")        → ["hühnchen","chicken"]
 */
export function expandSearchTerms(query: string): string[] {
  const tokens = query.toLowerCase().trim().split(/\s+/).filter(Boolean);
  const expanded = new Set<string>(tokens);

  // 1. Try full phrase
  const fullPhrase = tokens.join(' ');
  const phraseMatch = INGREDIENT_ALIASES[fullPhrase];
  if (phraseMatch) {
    expanded.add(phraseMatch);
    phraseMatch.split(' ').forEach(w => expanded.add(w));
  }

  // 2. Try 3-word combinations
  for (let i = 0; i < tokens.length - 2; i++) {
    const trigram = `${tokens[i]} ${tokens[i + 1]} ${tokens[i + 2]}`;
    const m = INGREDIENT_ALIASES[trigram];
    if (m) { expanded.add(m); m.split(' ').forEach(w => expanded.add(w)); }
  }

  // 3. Try 2-word combinations (bigrams)
  for (let i = 0; i < tokens.length - 1; i++) {
    const bigram = `${tokens[i]} ${tokens[i + 1]}`;
    const m = INGREDIENT_ALIASES[bigram];
    if (m) { expanded.add(m); m.split(' ').forEach(w => expanded.add(w)); }
  }

  // 4. Try individual tokens
  for (const token of tokens) {
    const m = INGREDIENT_ALIASES[token];
    if (m) { expanded.add(m); m.split(' ').forEach(w => expanded.add(w)); }
  }

  return Array.from(expanded);
}

/**
 * Resolve a single ingredient name to its English canonical.
 * Falls back to the original if no alias found.
 * Used when normalising shopping list ingredients from foreign-language recipes.
 */
export function resolveIngredientName(name: string): string {
  const lower = name.toLowerCase().trim();
  return INGREDIENT_ALIASES[lower] ?? name;
}
